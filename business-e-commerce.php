</h3>
						<p>Now – a-days E-commerce online websites were become very common and with this websites people earning a crore of rupees. Now we will discuss about the things that are more essential to build a website.</p>
						<p><h4>Ten things that every one should remember when building a website: </p></h4>

                        <p>
<br>1.	Start with your business name: In this section, we have to select a memorable and fabulous name which no one has already. You have to check whether the name is used or not in google. Once you select the name of the website you should register it.<br>

<br>2.	Secure your domain name and website: Ideally, you’ll get the same domain name as your business name, if not then choose a simple URL which can spell easily by people. The design of e-commerce website is the biggest expense you have. But you ensure that it not only visually appealing, but also perfect in functioning. There are out-of-the-box eCommerce solutions like shop to begin with, but you may require something more custom-made if your needs are more than basic.<br>

<br>3.	Select the business structure and register your business: you have got several options when it comes to business structure.              <br>                                                    
<br>•	Sole proprietor<br>
•	LLC<br>
•	Partnership<br>
•	Corporation<br>
<br>if you don’t want to choose corporation and LLC, you immediately take sole proprietor or partnership. If you choose sole proprietor, your personal assets are at risk. If your company is ever sued, the court can seize your personal assets if your business doesn’t have enough to cover its debt. Both the corporation and LLC separate you and your assets from the business and provide tax benefits.<br>



<br>4.	Get your Employer identification number:  you’ll need an Employer Identification Number (EIN) to open a business bank account and file your business taxes. Your EIN is just like a business security number: it’s a unique number that identifies your business and helps to file an important paperwork. Everyone needs one if you have employee or not. <br>

<br>5.	Apply for business license and permits: operating such e-commerce business doesn’t exclude you from having certain license and permits. Check with in your city, state and country to see what type of sales tax licenses and home business license you need, get them approved before you get operated.<br>


<br>6.	Find the right vendors: you’ll have a lot of competition in selling products so please keep it in mind that the materials you are used to make products is of best quality and the prices that you sell your products should be as minimum as possible.<br>

<br>7.	Start marketing early: even though your website is not online please made a coming soon page to attract people and people who attracted will get registered as soon as possible.<br>  


<br>8.	Get more productive with right software: Technology makes your work as simple as possible before you get online, operate with customer relationship management, email marketing software, accounting, project management and inform them what you will going to do once you get online.<br>

<br>9.	Stock your inventory: Whether you’ve got a warehouse full of products somewhere or your inventory lives in your garage, make sure you’ve got enough to launch. It can be tricky, not knowing how much you’ll need, but in general, it’s better to have too much inventory than not enough. Pay attention to how your sales increase so you can be smart with future orders.<br>

<br>10.	Make sure that your business stays compliant: 
Once you launch your eCommerce business, things are going to move at the speed of light. Don’t overlook filing your annual report, if you incorporated or filed an LLC, nor those annual fees for business permits. If you need to, put these deadlines on your calendar so you stay on top of them.<br>
